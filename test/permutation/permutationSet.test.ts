import { permutation } from "../../src/permutation/permutationSet";

describe("permutation()", () => {
  test("permutation([10, 30, 50,])", () => {
    expect(permutation([10, 30, 50])).toEqual([
      [10, 30, 50],
      [10, 50, 30],
      [30, 10, 50],
      [30, 50, 10],
      [50, 30, 10],
      [50, 10, 30],
    ]);
  });

  test("permutation([10, 30, 50, 70])", () => {
    expect(permutation([10, 30, 50, 70])).toEqual([
      [10, 30, 50, 70],
      [10, 30, 70, 50],
      [10, 50, 30, 70],
      [10, 50, 70, 30],
      [10, 70, 50, 30],
      [10, 70, 30, 50],
      [30, 10, 50, 70],
      [30, 10, 70, 50],
      [30, 50, 10, 70],
      [30, 50, 70, 10],
      [30, 70, 50, 10],
      [30, 70, 10, 50],
      [50, 30, 10, 70],
      [50, 30, 70, 10],
      [50, 10, 30, 70],
      [50, 10, 70, 30],
      [50, 70, 10, 30],
      [50, 70, 30, 10],
      [70, 30, 50, 10],
      [70, 30, 10, 50],
      [70, 50, 30, 10],
      [70, 50, 10, 30],
      [70, 10, 50, 30],
      [70, 10, 30, 50],
    ]);
  });
});
